#include<iostream>
using namespace std;

int main(){
    int num1, num2;
    cout<<"enter value of num1:"; // << is Insertion Operator
    cin>>num1; // >> is Extraction Operator
    cout<<"enter value of num2:";
    cin>>num2;
    cout<<"num1 is:"<<num1;
    cout<<"num2 is:"<<num2;
    cout<<"num1 + num2 is:"<<num1+num2;

   
    return 0;
}
